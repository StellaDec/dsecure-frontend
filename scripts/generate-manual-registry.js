import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const manualDir = path.resolve(__dirname, '../src/pages/support/manual');
const registryFile = path.resolve(__dirname, '../src/routes/manualRegistry.ts');

/**
 * Generate a slug from the file name
 * e.g., "AccessVerificationPage.tsx" -> "access-verification"
 */
function getSlug(fileName) {
  let name = fileName.replace('Page.tsx', '').replace('.tsx', '');
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase();
}

function run() {
  if (!fs.existsSync(manualDir)) {
    console.error(`Directory not found: ${manualDir}`);
    return;
  }

  const files = fs.readdirSync(manualDir).filter(f => f.endsWith('.tsx') && !f.includes('ManualPageRenderer'));
  console.log(`🔍 Found ${files.length} manual pages to register.`);

  let content = `// AUTOMATED MANUAL PAGE REGISTRY\n`;
  content += `// This file is generated by scripts/generate-manual-registry.js\n\n`;
  content += `import { lazy, LazyExoticComponent, ComponentType } from 'react';\n\n`;
  content += `export const MANUAL_COMPONENTS: Record<string, LazyExoticComponent<ComponentType<any>>> = {\n`;

  files.forEach(file => {
    const slug = getSlug(file);
    const componentName = file.replace('.tsx', '');
    content += `  '${slug}': lazy(() => import('../pages/support/manual/${componentName}')),\n`;
  });

  content += `};\n\n`;
  content += `export type ManualSlug = keyof typeof MANUAL_COMPONENTS;\n`;

  fs.writeFileSync(registryFile, content);
  console.log(`✅ Generated ${registryFile} with ${files.length} components.`);
}

run();
